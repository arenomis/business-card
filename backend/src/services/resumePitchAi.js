function replyForCategory(category, original) {
  const list = BODIES[category] || BODIES.default;
  const idx = hashPick(original, list.length);
  const lead = `По вашему вопросу «${snippet(original, 65)}»: `;
  const cta = CTA_VARIANTS[hashPick(original + category, CTA_VARIANTS.length)];
  return `${lead}${list[idx]} ${cta}`;
}

/**
 * Порядок важен: сначала более узкие темы.
 */
export function buildResumePitchAnswer(question) {
  const raw = String(question || '').trim();
  const q = raw.toLowerCase();

  if (
    /^(привет|здравств|добрый\s*(день|вечер))[,!.…\s]*$/i.test(raw.trim()) ||
    /^(hello|hi)\b[!?.…\s]*$/i.test(raw.trim())
  ) {
    return `Здравствуйте! Здесь ответы только про опыт Алексея из резюме: Angular, .NET, задачи в ММК, миграции, производительность интерфейсов. Задайте вопрос по делу — например про стек или кейсы. ${CTA_VARIANTS[hashPick(raw, CTA_VARIANTS.length)]}`;
  }

  if (raw.length < 2) {
    return `Задайте вопрос про опыт Алексея (стек, ММК, миграции, проекты). ${CONTACTS}`;
  }

  if (
    /(кто такой|представься|расскажи про себя|расскажи про себе|кратко о себе|кто вы такой|чем занимаешься|что делал в ммк|опиши себя|кто ты)\b/.test(
      q
    ) &&
    !/(путин|трамп|маск|зеленск|политик|погод|курс\s+евро|bitcoin|btc|крипт)/.test(q)
  ) {
    return replyForCategory('intro', raw);
  }

  if (!isOnResumeTopic(q)) {
    return offTopicReply(raw);
  }

  if (/(почему взять|почему нан|стоит ли|рекоменд|сильн|кандидат|собес|интервью|нанят|взять на работу)/.test(q)) {
    return replyForCategory('hire', raw);
  }
  if (/(миграц|легаси|legacy|верси|11|20|4\.7|dotnet|\.net\s*8|asp|поднял|обновил)/.test(q)) {
    return replyForCategory('migration', raw);
  }
  if (/(лес|таблиц|эксплуатац|скролл|оптимиз|производитель|быстр|батч|грид|grid)/.test(q)) {
    return replyForCategory('tables', raw);
  }
  if (/(большегруз|ремонт техники|мобил|mobile|с нуля|полев)/.test(q)) {
    return replyForCategory('trucks', raw);
  }
  if (/(поддержк|support|ux|обращен|баг|ошибк|пользовател)/.test(q)) {
    return replyForCategory('support', raw);
  }
  if (/(стек|технолог|angular|\.net|typescript|навык|что уме|webpack|vite|docker|prime|sql|c#)/.test(q)) {
    return replyForCategory('stack', raw);
  }
  if (/(ммк|mmk|корпоратив|20\s*000|20000|сотрудник|команда|промышлен|металл|магнитогорск)/.test(q)) {
    return replyForCategory('mmk', raw);
  }
  if (/(контакт|связ|telegram|почт|email|телефон|удалён|удален|релокац|b2|английск)/.test(q)) {
    return replyForCategory('contacts', raw);
  }
  if (/(cursor|chatgpt|ии|ai|нейросет|бот)/.test(q)) {
    return replyForCategory('tools', raw);
  }
  if (/(code review|ревью|git|gitlab|github|qa|тест|адаптив)/.test(q)) {
    return replyForCategory('code', raw);
  }

  return replyForCategory('default', raw);
}
