function hashPick(str, mod) {
  let h = 0;
  for (let i = 0; i < str.length; i += 1) {
    h = (h * 31 + str.charCodeAt(i)) >>> 0;
  }
  return h % mod;
}

function snippet(raw, max = 70) {
  let s = String(raw || '')
    .trim()
    .replace(/\s+/g, ' ')
    .replace(/[<>]/g, '');
  if (s.length > max) s = `${s.slice(0, max)}…`;
  return s || 'ваш вопрос';
}

